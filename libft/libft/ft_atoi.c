#include "libft.h"

int		ft_atoi(const char *str)
{
	int					c;
	unsigned long int	l;
	int					i;

	i = 0;
	c = 1;
	l = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		c = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		if ((l > 922337203685477580 || (l == 922337203685477580
										&& (str[i] - '0') > 7)) && c == 1)
			return (-1);
		else if ((l > 922337203685477580 || (l == 922337203685477580
		&& (str[i] - '0') > 8)) && c == -1)
			return (0);
		l = l * 10 + (str[i] - 48);
		i++;
	}
	return (l * c);
}
